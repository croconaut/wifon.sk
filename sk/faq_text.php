<p>
<p><b>V čom je WiFON lepší než napr. taký Facebook Messenger?</b> Rozdiel je hlavne "pod kapotou" -- zatiaľ čo bežné appky sú bez Internetu ako ryba na suchu, s <b>WiFON</b>-om
    môžeš komunikovať ďalej a ešte oveľa viac:</p>
<br>
<li>Chceš kamošovi poslať veľký súbor, ale nechceš si míňať paušál alebo jeden z vás nemá v mobile Internet -- <b>WiFON</b> automaticky nájde kamoša v tvojom okolí a pošle mu súbor rýchlejšie než akýkoľvek iný messenger!</li>
<li>Si niekde bez signálu, ale tvoj kamoš signál má. Napíš na svojom telefóne správu komu chceš a ona sa odošle cez kamošov telefón úplne automaticky.</li>
<li>Si na koncerte alebo festivale a mobilná sieť je preťažená. Pre <b>WiFON</b> platí -- čím viac mobilov, tým rýchlejšie sa správy doručujú!</li>
</p><br/>
<p>
    <b>Naozaj sa správy doručia aj bez Internetu? Ako je to možné?</b> Nie je to žiadna mágia, <b>WiFON</b> vytvára svoju vlastnú sieť a komunikuje cez ňu s okolitými zariadeniami. Je to trochu podobné tzv. hotspot režimu.
</p><br/>
<p>
    <b>Je komunikácia bezpečná? Nemôže si moje správy niekto prečítať?</b> Je pravda, že správy skáču zo zariadenia na zariadenie, ale to neznamená, že ich môže hocikto čítať.
    Každá správa je šifrovaná a môže ju dešifrovať iba adresát.
</p><br/>
